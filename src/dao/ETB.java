package dao;

public class ETB {
	
	private Integer idEscales;
	private String numeroEscale;
	private String dateCreation;
	private String nomFichier;
	
	public String getNomFichier() {
		return nomFichier;
	}
	public void setNomFichier(String nomFichier) {
		this.nomFichier = nomFichier;
	}
	public Integer getIdEscales() {
		return idEscales;
	}
	public void setIdEscales(Integer idEscales) {
		this.idEscales = idEscales;
	}
	public String getNumeroEscale() {
		return numeroEscale;
	}
	public void setNumeroEscale(String numeroEscale) {
		this.numeroEscale = numeroEscale;
	}
	public String getDateCreation() {
		return dateCreation;
	}
	public void setDateCreation(String dateCreation) {
		this.dateCreation = dateCreation;
	}
	

}
